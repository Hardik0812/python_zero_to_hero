# Arithmetic Operators
print(10+20) # print 30 is addition
print(10-20) # print -10 is subtraction
print(10*20) # print 200 is multiplication
print(10/3) # print 0.5 is division
print(10%3) # print 1 is modulus
print(10**3) # print 1000 is exponent
print(10//3) # print 3 is floor division

# working with numbers

print(round(2.3)) # print 2 is rounding up
print(abs(-2.9)) # print 2.9 is absolute value
print(pow(2,3)) # print 8 is power
print(max(2,3)) # print 3 is maximum
print(min(2,3)) # print 2 is minimum

